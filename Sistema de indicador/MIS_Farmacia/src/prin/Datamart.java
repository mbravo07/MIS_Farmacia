    void consultar() {

        //String producto = txtProducto.getText();
        String mes = (String) cbMes.getSelectedItem();
        String anio = (String) cbAnio.getSelectedItem();

        //String sql = "SELECT * FROM hec_preferencia";
        String sql = "SELECT nombre_producto, nom_lab, mes,anio, SUM(hecho) AS hecho"
                + " FROM hec_preferencia INNER JOIN dim_producto USING(id_producto)"
                + " INNER JOIN dim_laboratorio USING(id_laboratorio)"
                + " INNER JOIN dim_tiempo USING(fecha)"
                + " WHERE mes = '" + mes + "'"
                + " AND anio = '" + anio + "'"
                //+ " AND nombre_producto like '%'"+producto+"'%'"
                + " GROUP BY id_laboratorio";

        try {
            conect = con1.getConnection();
            st = conect.createStatement();
            rs = st.executeQuery(sql);
            Object[] thecho = new Object[5];
            modelo = (DefaultTableModel) tabla.getModel();
            while (rs.next()) {
                thecho[0] = rs.getString("NOMBRE_PRODUCTO");
                thecho[1] = rs.getString("NOM_LAB");
                thecho[2] = rs.getInt("MES");
                thecho[3] = rs.getInt("ANIO");
                thecho[4] = rs.getInt("HECHO");

                modelo.addRow(thecho);
            }
            tabla.setModel(modelo);

        } catch (Exception e) {
        }
    }

