    public void limpiartabla() {
        DefaultTableModel temp = (DefaultTableModel) tabla.getModel();
        int filas = tabla.getRowCount();

        for (int a = 0; filas > a; a++) {
            temp.removeRow(0);
        }
    }
    
    public void graficar(){
        DefaultPieDataset dtsc = new DefaultPieDataset();
        for (int i = 0; i < tabla.getRowCount(); i++) {
            
            dtsc.setValue(tabla.getValueAt(i, 1).toString(), Integer.parseInt(tabla.getValueAt(i, 4).toString()));
            JFreeChart ch = ChartFactory.createPieChart3D("Preferencias de Laboratorio", dtsc, true, true, false);
            ChartPanel cp = new ChartPanel(ch);
            add(cp);
            cp.setBounds(350, 138, 407, 380);           
            
        }
    }


